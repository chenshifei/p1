from random import random, choice

leettrans = {'a': ['4', '/-\\', '@', '/\\'],
             'b': ['8', '|3', '13'],
             'c': ['(', '{' , '[', '<'],
             'd': ['[)', ')', '[}', '|)', '|}', '|>'],
             'e': ['3'],
             'f': ['|=', 'ph'],
             'g': ['6', '(_+'],
             'h': ['#', '|-|', ')-(', '/-/', '|~|'],
             'i': ['1', '!', '|', '][', ':'],
             'j': ['_|', 'u|', ';_[['],
             'k': ['|<', '|{'],
             'l': ['|', '1', '|_'],
             'm': ['/\\/\\', '|\\/|', '[\\/]', '(\\/)', '/V\\'],
             'n': ['/\\/', '|\\|', '(\\)', '/|/', '[\\]'],
             'o': ['0', '()', '[]', '<>', '*'],
             'p': ['|D', '|*', '|>'],
             'q': ['(_,)', '0,', 'O,', 'O\\', '[]\\'],
             'r': ['|2', '|?', '/2'],
             's': ['5', '$'],
             't': ['7', '+', '7`', '~|~', '†'],
             'u': ['(_)', '|_|', '/_/', '\\_/'],
             'v': ['\\/', '\\\\//'],
             'w': ['\\/\\/', '|/\\|', '[/\\]', '(/\\)', 'VV', '\\^/', '\\\\/\\\//', '1/\\/', '\\/1/', '1/1/'],
             'x': ['><', '}{', ')('],
             'y': ["'/", '%', '`/', '\\j'],
             'z': ['2', '7_'],
}

def leet(text):
    result = ''
    for c in text:
        normalized_c = c.lower()
        if normalized_c in leettrans:
            i = random()
            if i < 0.6:
                subs = leettrans[normalized_c]
                result += choice(subs)
            else:
                result += c
        else:
            result += c
    return result

def leetloop():
    while True:
        text = input('Try a string!(Press \'X\' to exit)')
        if text == 'X':
            break
        else:
            print(leet(text))

leetloop()
